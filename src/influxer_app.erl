-module(influxer_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1, start/0]).

%%====================================================================
%% API
%%====================================================================

start() ->
    application:ensure_all_started(influxer).
    %influxer_sup:start_link().

start(_StartType, _StartArgs) ->
    influxer_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

